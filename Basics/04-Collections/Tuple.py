# Aim: Write a program to implement tuple operations

# Create a tuple
my_tuple = (1, 2, 3, 4, 5)

# Print tuple
print("Tuple:", my_tuple)

# Access element
print("Element at index 1:", my_tuple[1])

# Count occurrence
print("Count of 3:", my_tuple.count(3))

# Length of tuple
print("Length of tuple:", len(my_tuple))
