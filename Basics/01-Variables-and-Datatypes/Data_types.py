# Integer
a = 10
print("Integer:", a, type(a))

# Float
b = 10.5
print("Float:", b, type(b))

# Complex
c = 3 + 4j
print("Complex:", c, type(c))

# String
name = "Python"
print("String:", name, type(name))

# Boolean
is_valid = True
print("Boolean:", is_valid, type(is_valid))

my_list = [1, 2, 3, "Python"]
print("List:", my_list, type(my_list))

my_tuple = (10, 20, 30)
print("Tuple:", my_tuple, type(my_tuple))

my_range = range(5)
print("Range:", list(my_range), type(my_range))

my_set = {1, 2, 3, 3}
print("Set:", my_set, type(my_set))

my_frozenset = frozenset({1, 2, 3})
print("Frozenset:", my_frozenset, type(my_frozenset))

my_dict = {
    "name": "Python",
    "version": 3.12
}
print("Dictionary:", my_dict, type(my_dict))

x = None
print("NoneType:", x, type(x))
